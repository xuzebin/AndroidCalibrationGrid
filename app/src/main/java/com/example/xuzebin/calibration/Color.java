public class Color {
	public float r = 0.0f;
	public float g = 0.0f;
	public float b = 0.0f;
	public Color() {
		this.r = 0.0f;
		this.g = 0.0f;
		this.b = 0.0f;
	}

	public Color(float r, float g, float b) {
		this.r = r;
		this.g = g;
		this.b = b;
	}
	
}
